package Java1;

public class SwitchStatement14 {
	public static void main(String[] args) {
		// Switch Statement
		String gender = "Female3";
	switch (gender) {
		case "Female":
			System.out.println("I am a female");
			break;
		case "Male":
			System.out.println("I am a male");
			break;
		case "PreferNotToSsay":
			System.out.println("I am prefer not to say");
			break;
		default:
			System.out.println("Gender Not Known");

		}

	}
}
